public class For_mejorado {
    
    public static void main(String[] args) {
      
        int matriz[]={5,6,8,3,4,2,1};
        int suma=0;
        for (int numero :matriz) {
            
           suma+=numero;
            
        }
        System.out.println("Elemento :" + suma);
    
    
    
    
    }//main    
}
